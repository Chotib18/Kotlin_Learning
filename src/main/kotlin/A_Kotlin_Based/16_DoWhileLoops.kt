package A_Kotlin_Based

/*>>>>>>>Do While Loops
- Do While Loop adalah perulangan yang hampir sama dengan While Loop
- Yang membedakan adalah, pada Do While Loop, kode blok akan dijalankan dahulu,
  baru diakhir dilakukan pengecekan kondisi.

 */

fun main() {
    var i = 0;

    do {
        println("Chotibul Umam")
        i++
    } while (i < 10)
}